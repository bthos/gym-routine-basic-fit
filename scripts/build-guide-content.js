#!/usr/bin/env node
/**
 * Converts docs/llm-rutina-prompt.{es,en,be}.md to pre-rendered HTML fragments
 * bundled by GuideOverlay (no runtime Markdown library).
 *
 * Expands {{PROMPT_TEMPLATE}} from docs/llm-rutina-prompt-template.txt.
 *
 * Run: node scripts/build-guide-content.js
 * Hooked into predev/prebuild via package.json.
 */

const fs = require('fs');
const path = require('path');
const { marked } = require('marked');

const ROOT = path.join(__dirname, '..');
const OUT = path.join(ROOT, 'app', 'src', 'data', 'guideContent.js');
const TEMPLATE_PATH = path.join(ROOT, 'docs', 'llm-rutina-prompt-template.txt');

const LOCALES = ['es', 'en', 'be'];

marked.setOptions({ gfm: true, breaks: false });

const promptTemplate = fs.readFileSync(TEMPLATE_PATH, 'utf8');

const htmlByLocale = {};
for (const locale of LOCALES) {
  const mdPath = path.join(ROOT, 'docs', `llm-rutina-prompt.${locale}.md`);
  let md = fs.readFileSync(mdPath, 'utf8');
  md = md.replace(/\{\{PROMPT_TEMPLATE\}\}/g, promptTemplate);
  htmlByLocale[locale] = marked.parse(md);
}

const banner = `// Auto-generated by scripts/build-guide-content.js — do not edit by hand.
// Source: docs/llm-rutina-prompt.{es,en,be}.md + docs/llm-rutina-prompt-template.txt
`;

const body = `export const GUIDE_HTML = ${JSON.stringify(htmlByLocale, null, 2)};
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, banner + body, 'utf8');
console.log(`Wrote ${path.relative(ROOT, OUT)} (${LOCALES.length} locales)`);
